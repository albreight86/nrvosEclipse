package com.arb.db;

public class AdSize
{
   private int adSizeId;
   private int width;
   private int height;

   public int getAdSizeId()
   { return adSizeId; }

   public void setAdSizeId(int adSizeId)
   { this.adSizeId = adSizeId; }

   public int getWidth()
   { return width; }

   public void setWidth(int width)
   { this.width = width; }

   public int getHeight()
   { return height; }

   public void setHeight(int height)
   { this.height = height; }
}
